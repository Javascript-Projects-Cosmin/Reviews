  // Having our database of  reviews, let's think what is the scope of JS in this project. It is clearly to make it a dynamic page.
  //What is dynamic in this page is the change of reviews, so let's grab in some variables what is needed to change the review
  const img = document.getElementById("person-img");
  const author = document.getElementById("author");
  const job = document.getElementById("job");
  const info = document.getElementById("info");

  //Let's make some variables which grab the classes of the buttons we use on this simple page
  const prevBtn = document.querySelector(".prev-btn");
  const nextBtn = document.querySelector(".next-btn");
  const randomBtn = document.querySelector(".random-btn");
  
  // set starting item to nothing, so when the page starts, no review will be shown on the screen
  let currentItem = 0;
  
  // Now, this is a new event, related to the window, so the screen that basically opens up
  //What this does, is when the content is loaded does the next function which in a variable called
  //item, stores the currentItem Object of reviews and changes the variables we talked above to 
  //those of the currentItem Review.
  window.addEventListener("DOMContentLoaded", function () {
    const item = reviews[currentItem];
    img.src = item.img;
    author.textContent = item.name;
    job.textContent = item.job;
    info.textContent = item.text;
  });
  
  // Now we will make a function similar to the event above, which we will use on the button presses
  function showPerson(person) {
    const item = reviews[person];
    img.src = item.img;
    author.textContent = item.name;
    job.textContent = item.job;
    info.textContent = item.text;
  }
  // show next person
  nextBtn.addEventListener("click", function () {
    currentItem++;
    //this is to make sure the clicking of buttons can go in a circle, and not stop dead
    if (currentItem > reviews.length - 1) {
      currentItem = 0;
    }
    showPerson(currentItem);
  });
  // show prev person
  prevBtn.addEventListener("click", function () {
    currentItem--;
    if (currentItem < 0) {
      currentItem = reviews.length - 1;
    }
    showPerson(currentItem);
  });
  // show random person
  randomBtn.addEventListener("click", function () {
    currentItem = Math.floor(Math.random() * reviews.length);
    showPerson(currentItem);
  });